void kernel_main(bios_mmap_entry *mmap, u64 mmap_count) {
    // init IDT for reasons unbeknownst to man
    idt_init();
    sse_init();

    alloc_page(); // allocate the first page as this is where the stack lives, bootloader took care of setting it already

    u64 mmap_size = sizeof(bios_mmap_entry) * mmap_count;
    u64 page_count = (mmap_size + 4096 - 1) / 4096;
    for (u64 i = 0; i < page_count; i++) { // allocate memory for the mmap entries
        alloc_page();
    }

    paging_init();
    pmm_init(mmap, mmap_count);

    heap_start = alloc_page();
    for (u8 i = 0; i < 3; i++) {
        heap_end = alloc_page() + PAGE_SIZE;
    }
    heap_init();

    memset((void*)VGA_MEMORY, 0, 80 * 25 * 2); // clear screen

    enable_syscall();

    struct task user_program = {0};
    user_program.pid = 1;

    while (1) {
        __asm__ volatile ("hlt");
    }
}

void syscall_handler() {
    // currently does nothing
    static char syscall_message[] = "syscall has occured :O";

    for (u8 i = 0; syscall_message[i] != '\0'; i++) {
        VGA_MEMORY[i] = 0x0F00 | syscall_message[i];
    }

    while (1) {
        __asm__ volatile ("hlt");
    }
}

extern void syscall_stub();

void enable_syscall() {
    // enable syscall and sysret
    u64 IA32_EFER_VAL = read_msr(IA32_EFER);
    IA32_EFER_VAL |= 1;
    write_msr(IA32_EFER, IA32_EFER_VAL);

    // set syscall segments
    u64 STAR_MSR_VAL = (0x18 << 32) | (0x28 << 48) | 0;
    write_msr(STAR_MSR, STAR_MSR_VAL);

    // set syscall subroutine address
    u64 LSTAR_MSR_VAL = &syscall_stub;
    write_msr(LSTAR_MSR, LSTAR_MSR_VAL);

    // set flags reg mask to 0
    u64 SFMASK_MSR_VAL = 0;
    write_msr(SFMASK_MSR, SFMASK_MSR_VAL);
}

void sse_init() {
    // init SSE
    u32 edx;
    __asm__ volatile (
        "mov $0x1, %%eax\n\t"
        "cpuid\n\t"
        : "=d"(edx)
        :
        : "rax", "rbx", "rcx"
    ); // check if SSE is available otherwise PANIK
    if (!(edx & (1 << 25))) {
        panic("SSE not supported");
    }

    __asm__ volatile (
        "mov %%cr0, %%rax\n\t"
        "and $0xFFFB, %%ax\n\t" // clear coprocessor emulation CR0.EM
        "or $0x2, %%ax\n\t"     // set coprocessor monitoring  CR0.MP
        "mov %%rax, %%cr0\n\t"
        "mov %%cr4, %%rax\n\t"
        "or $0x600, %%ax\n\t" // set CR4.OSFXSR and CR4.OSXMMEXCPT at the same time
        "mov %%rax, %%cr4\n\t"
        :
        :
        : "rax"
    ); // SSE IS ENABLED
}

void paging_init() {
    pml4t = alloc_page();
    u64 *pdpt = alloc_page();
    u64 *pdt = alloc_page();
    u64 *pt = alloc_page();

    memset(pml4t, 0, PAGE_SIZE);
    memset(pdpt, 0, PAGE_SIZE);
    memset(pdt, 0, PAGE_SIZE);
    memset(pt, 0, PAGE_SIZE);

    u64 pml4_phys = v2p(pml4t);
    u64 pdpt_phys = v2p(pdpt);
    u64 pdt_phys = v2p(pdt);
    u64 pt_phys = v2p(pt);

    pml4t[511] = pdpt_phys | 0x03; // present + writable
    pdpt[510] = pdt_phys | 0x03;     // present + writable
    pdt[0] = pt_phys | 0x03;       // present + writable

    for (u64 i = 0; i < 512; i++) {
        pt[i] = (i * PAGE_SIZE) | 0x03; // present + writable
    }

    __asm__ volatile (
        "mov %0, %%cr3\n\t" // load PML4T physical address into CR3
        :
        : "r"(pml4_phys)
        : "memory"
    );
}

u64 v2p(void* v) {
    return (u64)v - KERNEL_BASE;
}